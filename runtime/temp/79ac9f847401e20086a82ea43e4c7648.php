                        亚航（曼谷廊曼国际机场） </br>

                        亚航作为东南亚廉价航空的代表，在曼谷投入了大量的航班方便世界各地游客出行，想到达曼谷，亚洲航空仍然是最高性价比的选择。中国民众可以方便快捷地从国内到达曼谷。亚航现已开通微信预定服务，乘客可以登陆关注亚航微信进行航班预定，暂时只对泰国航线开放。</br>

