                        大皇宫</br>

                        大皇宫（Grand Palace），又称大王宫或故宫。紧邻湄南河，是曼谷中心内一处大规模古建筑群（计28座），总面积二十一万八千四百平方米。作为曼谷最经典的旅行地点，无论你对曼谷作何期待，来到曼谷，总得来这里看看它的金碧辉煌。</br>

                        开放时间：08:30-16:30（15:20停止售票）</br>

                        四面佛</br>

                        四面佛（Erawan Shrine）在当地具有很高的地位，在东南亚，特别是泰国被认为是法力无边，掌握人间荣华富贵之神，其四面分别朝向东南西北，供信众祈福。位于曼谷中心区域的四面佛，常常吸引大量游客甚至是明星前来上香祈祷。</br>

                        开放时间：06:00-23:00</br>

                        郑王庙</br>

                        郑王庙（Wat Arun），又称黎明寺（Temple of the Dawn），是纪念泰国第41代君王、民族英雄郑昭的寺庙，始建于大城王朝。当你在夕阳就要西下的时候登上郑王庙，在顶上欣赏夕阳下的湄南河风光，美不胜收。郑王庙阶梯较为陡峭，在攀登的时候需要格外小心。</br>

                        开放时间：08:00-18:00</br>

                        美功铁路集市</br>

                        最特别的体验莫过于在美功铁路集市（Talat Mee Klong）看商贩们与火车互动的场景，当火车慢慢驶入集市路段，商贩们不紧不慢开始收起自己的摊位让火车通过。从曼谷市区到铁路集市约2小时的车程，也可以乘坐火车到达。</br>

                        到达方式：在英雄纪念碑车站乘坐小巴前往美功（Mee Klong），在美功的曼谷银行对面下车之后步行前往美功铁路集市</br>

                        安帕瓦水上市场</br>

                        要想体验纯正的曼谷水上市场，那么绝不能错过质朴的的安帕瓦水上市场（Amphawa Floating Market）。尽管越来越多的游客来到这里，安帕瓦水上市场还是保持着原有嘈杂繁忙的样子。若想体验这种别具一格的市场，不要错过这里。</br>

                        营业时间：周五至周日12:00-20:00</br>
