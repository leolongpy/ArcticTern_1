                        美食</br>

                        东南亚的美食已经无需过多赘述，水果、海鲜、各种奇怪的美食都能够在这里大快朵颐。</br>

                        冬阴功</br>

                        冬阴功的魅力想必在国内已经声名远播，无论你在中国多高档的泰式餐厅品尝到的冬阴功都不如在泰国当地所吃的那般地道。特色的配料和虾、蟹、鸡肉、猪肉一起熬制，酸辣的味道总是让人回味无穷。</br>

                        巧克力香蕉薄饼</br>

                        这种博饼常常出现在曼谷的大街小巷上，薄薄的饼皮配上当地的香蕉，淋上浓浓的巧克力酱，构成了这样一份具有当地特色的街头美食。</br>

                        芒果饭</br>

                        用水果来配饭这种特别的做法在泰国被发挥到了极致，香甜的芒果加上糯米构成了这样一道别具特色的菜肴。几乎所有来到泰国的游客，都会尝一尝。</br>

                        菠萝饭</br>

                        追求菠萝饭的根本就是海鲜炒饭用菠萝外壳盛放。但是这样简单的组合造就了它独特的味道，炒饭的香味以及菠萝的酸甜味强烈地组合在一起，让炒饭更加美味。</br>

                        水果</br>

                        来到泰国热带水果不可不尝。这里的小贩用冰块给水果保鲜，水果和冰块被共同放在一个巨大容器里，沿街叫卖，让人看上去就觉得凉爽。</br>



                        住宿</br>

                        一个热门的旅游城市自然可以满足各式各样的旅行者的需求，无论你是追求高性价比的背包客，亦或是休闲度假游的旅客，都能在这里找到心满意足的住所</br>

                        曼谷</br>

                        Miss Zhao Hostel（赵女士旅舍）</br>

                        考山路虽好但难免过于吵闹，选择距离适合住宿能让你在疯狂过后得到充分的休息。该青旅距离考山路600米，距离大皇宫约10分钟步行路程，十分便利，并且提供中文服务。</br>

                        CHERN Hostel（今晨旅馆）</br>

                        提供多种房型预定，从多人间到双人间，它都能满足你的需求。亲民的价格却配套了一应俱全的设施，提供了一个干净舒适的环境。</br>

                        Pathumwan Princess Hotel（曼谷帕色哇公主酒店）</br>

                        位于暹粒广场的豪华型五星级酒店，便利的交通环境和贴心的服务配套，非常追求品质的旅行者。</br>

                        华欣</br>

                        Soul Guesthouse（灵魂旅馆）</br>

                        位于华欣中心区域的一间旅馆，距离华欣夜市仅有两分钟的路程，步行六分钟便可到达海滩，距离华欣钟楼更是仅有100m的距离。酒店干净整洁，并且自带酒吧。</br>

                        G Hua Hin Resort & Mall（G华欣度假酒店及购物中心）</br>

                        舒适的客房拥有优雅的现代内饰和中性色调的墙壁，距离华欣海滩仅200米，是一处性价比极高的华欣度假酒店。</br>



                        泰国节庆</br>

                        万佛节</br>

                        万佛节（Makha Bucha）泰国的传统佛教节日，为了纪念佛祖信徒对于佛祖的信仰与尊敬，在每年泰历三月十五日举行。万佛节当天的活动包括：清晨僧侣行善；下午聆听诵经讲道；傍晚僧侣约定时刻鸣钟集合，举行礼佛仪式；并在全国各地悬挂国旗。</br>

                        时间：每年泰历三月十五（闰年泰历四月十五日）</br>

                        泼水节</br>

                        又称宋干节（Songkra）n，也是泰国新年。泰国泼水节代表着清除所有的邪恶、不幸和罪恶，并怀着一切美好和纯净开始新的一年。一年一度的泼水节就像中国新年一样热闹非凡，在曼谷会举行大型庆典及各项庆祝活动。大街小巷中所有人都忙着泼水，互道恭喜及祝福，尤其在炎热的天气下，淋一身湿反而更能消暑。推荐到曼谷著名的小街去玩水，如Kaosarn Road、Silom Road。</br>

                        时间：每年4月13日，历时三天。</br>

                        佛诞节</br>

                        尽管佛祖诞生、悟道、涅盘的年份各异，但日期却相同，都是发生在泰历六月十五日。其节庆活动包括：佛教徒须于清晨前往佛寺与僧侣吃斋布施，聆听高僧传经讲道；民众捧香烛鲜花参加佛寺的巡烛礼；各处悬挂国旗。</br>

                        时间：每年泰历六月十五（闰年泰历七月十五）</br>

                        春耕节</br>

                        每年五月正值稻米播种季节，皇室会在皇家田举行插秧播种大典。参与这项皇家盛会的人们，穿著色彩鲜艳的衣服，分别举行不同的仪式，藉此祈求来年能够丰收，是泰国传统节日之一。</br>

                        时间：每年5月某吉日，由国王选定。</br>

                        三宝节</br>

                        泰国守夏节前一日，因这一天是佛祖成道后首次讲道的日子、有了第一个佛门弟子的日子、世上出现了第一个和尚的日子，是佛教“三宝”（佛、法僧）都齐备的日子而得名。当天全国佛教徒、各佛寺届时都做仪式，如守戒、听经、诵经、讲道、巡烛等。</br>

                        时间：泰历八月十五</br>

                        守夏节</br>

                        泰历八月十六日，标志着佛教斋期开始，历时三个月（泰历八月十六日至十一月十五日），守夏节结束之日称“解夏节”。在此期间，僧侣均驻守在寺庙中不外出。</br>

                        时间：每年泰历八月十六</br>

                        兰娜赛舟</br>

                        十月是兴奋刺激的兰娜赛舟的月份。比赛时，色彩缤纷的船艇互相斗智斗力。</br>

                        时间：每年十月</br>

                        水灯节</br>

                        水灯节是泰国的主要节日，亦是泰国民间最热闹、最富诗意的传统节日，不亚于泼水节，在民间流行甚广。一般民众自行制作水灯，点缀以花、烛，然后漂放在池河中，寄托对滋润万物的水精灵的感谢之情，同时也希望所有的罪恶和污秽能够顺水漂走。水灯节的夜晚，月圆如盘，风清若丝，江河湖溪，漂满万盏水灯，烛光与笑脸明月相辉映，充满诗情画意。</br>

                        时间：每年泰历十二月十五</br>

                        万寿节</br>

                        泰王蒲美蓬陛下诞生于1927年12月5日，于1946年6月9日继承王业，成为查库里王朝第九世君王，故定12月5日为万寿节，也为泰国的父亲节。当天的节日活动有：各处悬挂国旗；举行佛教仪式；吃斋行善；恭祝圣寿；举办福利事业活动等。</br>

                        时间：每年12月5日</br>
