                        签证</br>

                        泰国签证为纸签及落地签，纸签签证流程较为简单，准备好有效的护照及其他资料即可办理，也可以委托具有资质的旅行社代办；落地签是泰国政府推出的签证政策，游客可以持空白护照抵达泰国后进行签证，自2016年9月27日起，落地签费用为2000泰铢，允许停留15天。</br>

                        时间</br>

                        泰国位于东7区，与世界标准时间相差7个小时，与北京时间相差一小时。</br>

                        货币</br>

                        泰国的官方货币为泰铢，缩写为THB，读音为“巴”，中国人民币兑泰铢约为1:5.2（2016年9月）</br>

                        插头</br>

                        泰国用的是德标插座，电压为220伏特，与中国一致。</br>

                        天气</br>

                        泰国属于热带气候，四季都是夏天，平均气温在20-31摄氏度之间，每年6月-10月为当地雨季。无论是否是雨季前往，你都应该准备雨具以应对当地多变的天气。</br>
