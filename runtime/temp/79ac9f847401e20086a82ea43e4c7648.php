                    <div class="modal-body">
                        曼谷拥挤的交通以及炎热的气候着实让人头痛，往往选择通过当地的tutu车或者轨道交通到达目的地，却因此错过了曼谷最地道的市井文化，倘若你在曼谷租赁一辆自行车骑行于曼谷的大街小道，想必能收获有别于普通旅行者的经历。</br>

                        曼谷骑行道</br>

                        曼谷政府之前推出的自行车计划Bangkok Smile Bike，在老城区及沿河区域设立免费的自行车租赁点，供市民游客使用，并修建了两条自行车道，分别为老城区线（Rattanakosin Route）和西部沿河线（West Side）。尽管在2015年时Bangkok Smile Bike计划因为某些原因终止了，但游客及市民仍可以沿着这两条线路骑行。</br>

                        老城区线（Rattanakosin Route）</br>

                        老城区的线路经过的路线为：Ratchadamnoen Klang Avenue-Tanao Road-Bamrung Muang, Kalayana Maitri-Sanam Chai-Thai Wang-Maha Rat-Na Phra Lan-Na Phra That-Rachini-Phra Athit-Phra Sumen-Sip Sam Hang Roads，全程约8公里。经过了大部分位于老城区的景点，其中包括：大皇宫、卧佛寺、巴苏孟要塞和民主纪念碑等。更重要的是，当你骑着自行车穿梭于嘈杂的车流之间，却依然可以收获曼谷闹市的宁静。</br>

                        西部沿河线（West Side）</br>

                        这条线路有一段沿着湄公河骑行的路段，但是最大的特点还是在于它穿梭于泰国民众的生活圈之中，不经意间便能发现最地道的泰国场景，哭闹的孩提，静坐的老妪甚至是漫步的僧侣，你都可以在这段不长的骑行线路中相遇。</br>

                        素万那普机场自行车道</br>

                        曼谷政府在2014年起修建了一条围绕着素万那普机场的自行车骑行绿道（Suvarnabhumi Airport Cycle Trail），全长约23公里，单向通行，配备有卫生间。如果你是一个狂热的骑行爱好者，大可以带上你的自行车来到曼谷，体验一下这条位置独特的自行车专用道。值得注意的是，自行车道的开放时间为当地时间早上六点到晚上六点，现场不提供自行车租赁。</br>

                        曼谷骑行</br>

                        倘若觉得自己去追溯自行车道过于繁琐，你大可以在曼谷直接找商业定制的自行车漫游，这些贴心的商户早就给来到曼谷旅行的游客提供了一系列单车旅行一日游选择，由专业的向导带你走遍曼谷的大街小巷、乡村郊野，带你领略一个不一样的曼谷。一次单车旅行的费用大约为1000-2000泰铢。</br>
