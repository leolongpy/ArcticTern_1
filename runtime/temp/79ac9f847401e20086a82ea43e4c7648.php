                        曼谷机场</br>

                        曼谷一共有两个国际机场，分别为廊曼国际机场（DMK）和素万那普国际机场（BKK）。两个机场距离较远，搭乘飞机时注意看清航班的起降机场。</br>

                        廊曼国际机场</br>

                        廊曼国际机场（Don Mueang International Airport）是曼谷的旧机场，是廉价航空公司以及泰国国内航班的重要枢纽。亚洲航空公司的航班都在廊曼国际机场（DMK）起降。除了亚洲航空之外，飞鸟航空、泰国东方航空、太阳航空等都有航班在廊曼国际机场起降。</br>

                        从廊曼机场到素万那普国际机场</br>

                        廊曼机场距素万那普国际机场约50公里。出租车仍然是最快、最方便的交通方式，平均耗时约 45 分钟。也可乘坐554或555路巴士。</br>

                        素万那普国际机场</br>

                        素万那普国际机场（Suvarnabhumi International Airport）是曼谷的新机场，是曼谷的主要民用机场，也是东南亚地区乃至亚洲地区重要的航空枢纽，除了泰国国际航空之外，国泰航空、中华航空、长荣航空等外籍航空公司也在这里起降。</br>
