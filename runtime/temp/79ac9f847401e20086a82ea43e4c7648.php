            <h5>简介</h5>
            <span>曼谷（泰文：กรุงเทพมหานคร；英文：Bangkok），是泰国首都和最大城市，别名“天使之城”，位于昭披耶河东岸，南临暹罗湾，中南半岛最大城市，东南亚第二大城市，为泰国政治、经济、贸易、交通、文化、科技、教育、宗教与各方面中心。
                一座以佛教文化闻名的城市，却囊括了各种户外的元素，潜水、徒步、骑行等户外方式，你都能在这里体验，享受这座五颜六色、时常塞车的国际大都市的户外时刻。
